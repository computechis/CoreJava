package comments;

public class MultiLineComments {

	public static void main(String[] args) {

		System.out.println("Selenium have 4 components");
		/*
		 To comment the block of statements : Ctrl + Shift + /
		 To uncomment the block of commented statements : Ctrl + Shift + \
		 */
		System.out.println("Selenium IDE");
		System.out.println("Selenium RC");
		System.out.println("Selenium WebDriver");
		System.out.println("Selenium GRID");

	}

}
